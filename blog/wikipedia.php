                  Wikipedia is a live collaboration differing from paper-based reference sources in important ways. It is continually created and updated, with articles on new events appearing within minutes, rather than months or years. Because everybody can help improve it, Wikipedia has become more comprehensive than any other encyclopedia. Besides quantity, its contributors work on improving quality, removing or repairing misinformation and other errors. Over time, articles tend to become more comprehensive and balanced. However, because anyone can click "edit" at any time and add content, any article may contain undetected misinformation, errors, or vandalism.<br>  

