                  Wikipedia is written collaboratively by largely anonymous volunteers who write without pay. Anyone with internet access can write and make changes to Wikipedia articles, except in limited cases where editing is restricted to prevent disruption or vandalism.<br>
                  
                  Since its creation on January 15, 2001, Wikipedia has grown into the world's largest reference website, attracting 1.5 billion unique visitors monthly as of March 2020. It currently has more than 53 million articles in more than 300 languages, including 6,098,095 articles in English with 143,662 active contributors in the past month.<br>

